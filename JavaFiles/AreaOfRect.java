package JavaBeginners.JavaFiles;

public class AreaOfRect {
    public static void main(String[] args) {
        int l = 10;
        int w = 6;
        int area = l * w;

        System.out.println("Length is: "+ l);
        System.out.println("Width is: "+ w);
        System.out.println("Area of the rectangle is "+ area);
    }
}
