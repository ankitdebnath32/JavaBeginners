package JavaFiles;

public class ForLoop {

    public static void main(String[] args) {
        for(int i = 0; i<=10; i++){
            System.out.println(i);
        }

        String[] cars = {"Audi","BMW","Lambo","Mercedes"};
        for (String i : cars){
            System.out.println(i);
        }
    
    }
}
